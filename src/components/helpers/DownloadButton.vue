<template>
  <v-btn
    icon
    color="grey"
    @click.stop="download"
  >
    <v-icon>mdi-download-circle-outline</v-icon>
  </v-btn>
</template>

<script>
import { resourceURL } from '@/helpers/resourceHelper';

export default {
  props: {
    hash: {
      type: String,
      required: true,
    },
    title: {
      type: String,
      required: false,
      default: 'download',
    },
  },
  methods: {
    download() {
      fetch(resourceURL(this.hash))
        .then((response) => response.blob())
        .then((blob) => {
          const url = window.URL.createObjectURL(blob);
          const link = document.createElement('a');
          link.href = url;
          link.download = this.title;
          link.click();
        })
        .catch(console.error);
    },
  },
};
</script>
